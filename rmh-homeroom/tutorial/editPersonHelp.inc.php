                <p> <strong>How to Edit a Person's Information in the Database</strong>
                <p> Editing information in the volunteer database is usually done by the House Manager.  There is one exception: a volunteer can view and modify his or her own information
                in the database (but nobody else's).  So this tutorial is mainly for the House Manager.
				<p> <strong>Step 1:</strong> First you need to either <strong>view</strong> or <strong>search</strong> for the person whose information you want to edit.
				    <BR>
					After finding that person, select <strong> edit</strong> beside his/her name.


				<p> <strong>Step 2:</strong> You will now see a page with all of the person's information, like this:<BR><BR>
                     <a href="tutorial/screenshots/editHelp1.png" class="image" title="searchPersonStep1.png"
                     	target="tutorial/screenshots/editHelp1.png">
					 &nbsp&nbsp&nbsp&nbsp<img src="tutorial/screenshots/editHelp1.png" width="10%" border="1px" align="middle"></a></p>
				<p> <strong>Step 3:</strong> To change any of that information, just retype or reselect it.  For instance, to change Availability,
						select one of the unchecked (checked) boxes and it will become checked (unchecked).
					<BR>&nbsp&nbsp REMEMBER: No fields marked by <font color="#FF0000">*</font> can be left blank.
				</p>

				<p> <strong>Step 4:</strong> When you are finished making changes, select <strong>Submit</strong> at the bottom of the page:<BR><BR>
				     <a href="tutorial/screenshots/addpersonstep3.png" class="image" title="addPersonStep3.png"
				     	target="tutorial/screenshots/searchPersonStep3.png">
					 &nbsp&nbsp&nbsp&nbsp<img src="tutorial/screenshots/addpersonstep3.png" width="10%" border="1px" align="middle"></a></p>

				<p> <B>Step 5:</B> If an error was made, <font color=#FF0000>red</font>
				warnings will tell you what needs to be corrected, like this:<BR><BR>
				<a href="tutorial/screenshots/addpersonstep4.png" class="image" title="addPersonStep4.png"
					target="tutorial/screenshots/searchPersonStep4.png" horizontalalign="center">
					 &nbsp&nbsp&nbsp&nbsp<img src="tutorial/screenshots/addpersonstep4.png" width="10%" border="1px" align="middle"></a>
                <BR>&nbsp&nbsp&nbsp *After you have made these corrections, repeat <B>Step 4</B>.

				<p> <B>Step 6:</B> If you have no errors or omissions, you will see a page telling you the edit was successful, like this:<BR><BR>
					<a href="tutorial/screenshots/addpersonstep5.png" class="image" title="addPersonStep5.png"
