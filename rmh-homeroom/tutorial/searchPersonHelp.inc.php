<p> <strong>How to Search for People in the Database</strong>
				<p> <B>Step 1:</B> On the navigation bar at the top of the page, find <B>people:</B> and select <B>search</B>, like this:<BR><BR>
				  <a href="tutorial/screenshots/searchPersonStep1.png" class="image" title="searchPersonStep1.png"
					target="tutorial/screenshots/searchPersonStep1.png">
					 &nbsp&nbsp&nbsp&nbsp<img src="tutorial/screenshots/searchPersonStep1.png" width="10%" border="1px" align="middle"></a></p>
				<p> <B>Step 2:</B> You can enter a first name, a last name, or any part of a person's notes as a search criterion.
				                   After typing your criterion in the appropriate box, select the <B>Search</B> button, like this:<BR><BR>
				  <a href="tutorial/screenshots/searchPersonStep2.png" class="image" title="searchPersonStep2.png"
					target="tutorial/screenshots/searchPersonStep2.png">
					&nbsp;&nbsp;&nbsp;&nbsp;<img src="tutorial/screenshots/searchPersonStep2.png" width="10%" border="1px" align="middle"></a></p>
				<p> <B>Step 3:</B> Now you will see a list of the names of persons in the database that match your search criterion, like this:<BR><BR>
				  <a href="tutorial/screenshots/searchPersonStep3.png" class="image" title="searchPersonStep3.png"
				  	target="tutorial/screenshots/searchPersonStep3.png">
				 	&nbsp&nbsp&nbsp&nbsp<img src="tutorial/screenshots/searchPersonStep3.png" width="10%" border="1px"  align="middle"></a></p>
				  <p> If this list has the name of the person you want to view or edit, then select <B>view</B> or <B>edit</B> or
									 to the right of that name.  <br>

				<p> <B>Step 4:</B> If you don't see what you were looking for, you can try again again.  The
									search box always reappears at the bottom of the page below the results, like this:<BR><BR>
				  <a href="tutorial/screenshots/searchPersonStep4.png" class="image" title="searchPersonStep4.png"
					horizontalalign="center" target="tutorial/screenshots/searchPersonStep4.png">
					&nbsp&nbsp&nbsp&nbsp<img src="tutorial/screenshots/searchPersonStep4.png" width="10%" border="1px" align="middle"></a></p>
				<p> <B>Step 5:</B> Other search options include searching for all persons with a particular status, like "active" or "LOA".<BR><BR>
				  <a href="tutorial/screenshots/searchPersonStep6.png" class="image" title="searchPersonStep6.png"
					horizontalalign="center" target="tutorial/screenshots/searchPersonStep6.png">
					&nbsp&nbsp&nbsp&nbsp<img src="tutorial/screenshots/searchPersonStep6.png" width="10%" border="1px" align="middle"></a></p>
				<p> You can also search for all volunteers for a particular venue, like "Family room" or "Van driver".<BR><BR>
				  <a href="tutorial/screenshots/searchPersonStep5.png" class="image" title="searchPersonStep5.png"
					horizontalalign="center" target="tutorial/screenshots/searchPersonStep5.png">
					&nbsp&nbsp&nbsp&nbsp<img src="tutorial/screenshots/searchPersonStep5.png" width="10%" border="1px" align="middle"></a></p>
				<p> <B>Step 6:</B> When you finish, you can return to any other function by selecting it on the navigation bar.
